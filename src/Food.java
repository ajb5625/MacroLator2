public class Food {
    public int calories;
    public int protein;
    public int carbs;
    public int fats;
    public String name;

    public Food(int calories, int protein, int carbs, int fats, String name) {
        this.calories = calories;
        this.protein = protein;
        this.carbs = carbs;
        this.fats = fats;
        this.name = name;
    }
}
